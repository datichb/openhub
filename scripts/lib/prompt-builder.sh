# Résout la liste des skills stack à injecter pour un agent donné.
# @param $1 — agent_id (ex: developer-frontend)
# @param $2 — stacks_list (liste de clés séparées par des espaces ou newlines)
# @param $3 — stack_skills_config (chemin vers config/stack-skills.json)
# Affiche sur stdout les chemins de skills à injecter, un par ligne, dédupliqués.
resolve_stack_skills() {
  local agent_id="$1"
  local stacks_list="$2"
  local config_file="$3"

  [ -z "$stacks_list" ] && return 0
  [ -f "$config_file" ] || return 0
  command -v jq &>/dev/null || return 0  # jq requis pour parser le JSON

  # Récupérer les catégories autorisées pour cet agent
  local allowed_categories
  allowed_categories=$(jq -r --arg agent "$agent_id" \
    '._agent_scope[$agent] // [] | .[]' "$config_file" 2>/dev/null)

  # Si pas de scope défini pour cet agent, ne rien injecter
  [ -z "$allowed_categories" ] && return 0

  local seen=()
  local stack

  while IFS= read -r stack; do
    [ -z "$stack" ] && continue

    # Pour chaque catégorie autorisée, chercher si la stack y est mappée
    local category
    while IFS= read -r category; do
      [ -z "$category" ] && continue

      # Chercher la stack dans cette catégorie
      local skills
      skills=$(jq -r --arg cat "$category" --arg stack "$stack" \
        '.[$cat][$stack] // [] | .[]' "$config_file" 2>/dev/null)

      [ -z "$skills" ] && continue

      # Ajouter les skills non encore vus
      local skill
      while IFS= read -r skill; do
        [ -z "$skill" ] && continue
        local already_seen=0
        local s
        for s in "${seen[@]:-}"; do
          [ "$s" = "$skill" ] && already_seen=1 && break
        done
        if [ "$already_seen" = "0" ]; then
          seen+=("$skill")
          echo "$skill"
        fi
      done <<< "$skills"
    done <<< "$allowed_categories"
  done <<< "$stacks_list"
}

# Précalcule en une seule passe jq tous les stack skills pour une liste de stacks détectées.
# Retourne une chaîne "agent_id:skill_path\n..." utilisable par _get_precomputed_stack_skills.
# Signature : precompute_stack_skills STACKS_NEWLINE_SEP CONFIG_FILE
# Exemple d'usage :
#   _cached=$(precompute_stack_skills "$detected_stacks" config/stack-skills.json)
#   while IFS= read -r sk; do ...; done < <(_get_precomputed_stack_skills "developer-api" "$_cached")
precompute_stack_skills() {
  local stacks_list="$1"
  local config_file="$2"

  [ -z "$stacks_list" ] && return 0
  [ -f "$config_file" ] || return 0
  command -v jq &>/dev/null || return 0

  # Construire un tableau jq de stacks depuis la liste bash (une seule invocation jq — zéro fork supplémentaire)
  local stacks_json
  stacks_json=$(printf '%s\n' "$stacks_list" | jq -Rs '[split("\n")[] | select(. != "")]')

  # En une seule invocation jq : pour chaque agent qui a un _agent_scope,
  # parcourir les catégories autorisées, puis les stacks détectées,
  # et émettre "agent_id:skill_path" pour chaque skill trouvé.
  jq -r --argjson stacks "$stacks_json" '
    . as $conf |
    ($conf._agent_scope // {}) | to_entries[] |
    select(.value | type == "array") |
    .key as $agent |
    .value[] as $cat |
    $stacks[] as $stack |
    ($conf[$cat][$stack] // [])[] |
    "\($agent):\(.)"
  ' "$config_file" 2>/dev/null | sort -u || true
}

# Filtre les lignes "agent_id:skill" précalculées pour un agent donné.
# Signature : _get_precomputed_stack_skills AGENT_ID PRECOMPUTED_STRING
_get_precomputed_stack_skills() {
  local agent_id="$1"
  local precomputed="$2"
  [ -z "$precomputed" ] && return 0
  local prefix="${agent_id}:"
  while IFS= read -r line; do
    [ "${line#"$prefix"}" != "$line" ] && printf '%s\n' "${line#"$prefix"}"
  done <<< "$precomputed"
}

# Retourne le nom d'une skill au format opencode (kebab-case, basename sans .md)
# Conforme à la contrainte de nommage opencode : ^[a-z0-9]+(-[a-z0-9]+)*$
# Ex: developer/stacks/dev-standards-vuejs → dev-standards-vuejs
# Ex: auditor/audit-security               → audit-security
to_skill_name() {
  local skill_path="$1"
  basename "$skill_path" .md
}

# Génère la section "## Skills disponibles" dans le body de l'agent.
# Liste les skills natives chargeable à la demande via l'outil skill d'opencode.
# @param $@ — liste de chemins de skills relatifs à SKILLS_DIR
#             (ex: "developer/dev-standards-frontend" "developer/stacks/dev-standards-vuejs")
_build_native_skills_guide() {
  [ $# -eq 0 ] && return 0
  local _native_list=("$@")

  echo ""
  echo "---"
  echo ""
  echo "## Skills disponibles"
  echo ""
  echo "Charge les skills pertinentes **avant de commencer** via l'outil \`skill\`."
  echo "Sélectionne uniquement celles qui correspondent au contexte de la tâche en cours."
  echo ""
  echo "| Skill | Description |"
  echo "|-------|-------------|"

  local skill skill_name skill_file desc
  for skill in "${_native_list[@]}"; do
    [ -z "$skill" ] && continue
    skill_name=$(to_skill_name "$skill")
    skill_file="$SKILLS_DIR/${skill}.md"
    if [ -f "$skill_file" ]; then
      desc=$(extract_frontmatter_value "$skill_file" "description")
    else
      desc=""
    fi
    [ -z "$desc" ] && desc="—"
    echo "| \`${skill_name}\` | ${desc} |"
  done
}

# Construit le contenu final : corps de l'agent + skills injectés → stdout
# $2 (lang) optionnel — si non vide, injecte une instruction de langue en tête de l'agent
# $3 (project_path) optionnel — si non vide, détecte la stack et prépare les skills stack
# $4 (precomputed_stacks) optionnel — si non vide, utilise les stack skills précalculés (chemin rapide)
#   fourni par adapter_deploy_files via precompute_stack_skills ; zéro fork supplémentaire.
#   Si vide, fallback sur detect_stack + resolve_stack_skills (rétrocompatibilité).
#   Note : ignoré silencieusement si $3 (project_path) est vide ou inexistant.
#
# Architecture hybride :
#   - skills:        → inlinées dans le body (Bucket A, obligatoires, toujours actives)
#   - native_skills: → listées dans le guide "Skills disponibles" (Bucket B, chargement à la demande)
#   - stack skills   → listées dans le guide "Skills disponibles" (Bucket B, dynamiques)
build_agent_content() {
  local agent_file="$1"
  local lang="${2:-}"
  local project_path="${3:-}"
  local precomputed_stacks="${4:-}"
  [ -f "$agent_file" ] || { log_warn "Agent introuvable : $agent_file" >&2; return 1; }

  build_generated_header
  echo ""
  if [ -n "$lang" ]; then
    echo "> **Langue de travail : ${lang}.** Rédige toutes tes réponses, rapports et commentaires en ${lang}, quelle que soit la langue des instructions ci-dessous."
    echo ""
  fi
  strip_frontmatter "$agent_file"

  # ── Skills INLINE : déclarés dans le frontmatter (Bucket A — obligatoires) ──
  # Ces skills sont injectées directement dans le system prompt de l'agent.
  # Le LLM les reçoit TOUJOURS — protocoles, workflows, handoffs, chaînes de délégation.
  local skills=()
  while IFS= read -r skill; do
    skills+=("$skill")
  done < <(extract_frontmatter_list "$agent_file" "skills")

  # ── Skills NATIVES : déclarées dans le frontmatter (Bucket B — optionnelles) ──
  # Ces skills sont déployées dans .opencode/skills/<name>/SKILL.md et chargées à la demande.
  # Elles n'apparaissent PAS dans le system prompt — seulement dans le guide ci-dessous.
  local native_skills_from_fm=()
  while IFS= read -r ns; do
    native_skills_from_fm+=("$ns")
  done < <(extract_frontmatter_list "$agent_file" "native_skills")

  # ── Stack skills : résolues dynamiquement (Bucket B — optionnelles) ──────────
  # Déployées en .opencode/skills/ selon la stack détectée dans le projet cible.
  # Ne sont plus inlinées depuis la migration vers l'architecture hybride.
  local stack_skills=()
  if [ -n "$project_path" ] && [ -d "$project_path" ]; then
    local agent_id
    agent_id=$(get_agent_id "$agent_file")
    local stack_skills_config="${HUB_DIR:-}/config/stack-skills.json"
    if [ -f "$stack_skills_config" ]; then
      if [ -n "$precomputed_stacks" ]; then
        # Chemin rapide : utiliser les stack skills précalculés
        while IFS= read -r stack_skill; do
          [ -z "$stack_skill" ] && continue
          # Dédupliquer contre les skills déjà déclarées (inline ou native)
          local already_declared=0
          local declared_skill
          for declared_skill in "${skills[@]:-}" "${native_skills_from_fm[@]:-}"; do
            [ "$declared_skill" = "$stack_skill" ] && already_declared=1 && break
          done
          [ "$already_declared" = "0" ] && stack_skills+=("$stack_skill")
        done < <(_get_precomputed_stack_skills "$agent_id" "$precomputed_stacks")
      else
        # Chemin lent (rétrocompatibilité) : détecter la stack à la volée
        local detected_stacks
        detected_stacks=$(detect_stack "$project_path" | sort -u || true)
        if [ -n "$detected_stacks" ]; then
          while IFS= read -r stack_skill; do
            [ -z "$stack_skill" ] && continue
            local already_declared=0
            local declared_skill
            for declared_skill in "${skills[@]:-}" "${native_skills_from_fm[@]:-}"; do
              [ "$declared_skill" = "$stack_skill" ] && already_declared=1 && break
            done
            [ "$already_declared" = "0" ] && stack_skills+=("$stack_skill")
          done < <(resolve_stack_skills "$agent_id" "$detected_stacks" "$stack_skills_config")
        fi
      fi
    fi
  fi

  # ── Injection des skills INLINE (obligatoires) ───────────────────────────────
  if [ ${#skills[@]} -gt 0 ]; then
    echo ""
    echo "---"
    echo ""
    for skill in "${skills[@]}"; do
      [ -z "$skill" ] && continue
      local skill_file="$SKILLS_DIR/${skill}.md"
      if [ -f "$skill_file" ]; then
        strip_frontmatter "$skill_file"
        echo ""
      else
        log_warn "Skill introuvable : ${skill}.md" >&2
      fi
    done
  fi

  # ── Guide des skills natives (Bucket B — chargement à la demande) ────────────
  # Injecte un tableau listant les skills natives + stack skills disponibles.
  # Le LLM voit la liste mais charge uniquement les skills utiles via l'outil `skill`.
  local all_native=("${native_skills_from_fm[@]:-}" "${stack_skills[@]:-}")
  if [ ${#all_native[@]} -gt 0 ]; then
    _build_native_skills_guide "${all_native[@]}"
  fi
}

# Construit le prompt de bootstrap pour le mode --dev (oc start --dev)
# Interroge bd ready dans le répertoire du projet
# @param $1 — project_path
# @param $2 — label (optionnel, défaut : "ai-delegated")
# @param $3 — assignee (optionnel, si fourni filtre par assignee au lieu du label)
build_dev_bootstrap_prompt() {
  local project_path="$1"
  local label="${2:-ai-delegated}"
  local assignee="${3:-}"
  local tickets

  if [ -n "$assignee" ]; then
    tickets=$(cd "$project_path" && bd ready --assignee "$assignee" --json 2>/dev/null) || tickets="[]"
  else
    tickets=$(cd "$project_path" && bd ready --label "$label" --json 2>/dev/null) || tickets="[]"
  fi
  # Valider que la sortie est un JSON array — si bd retourne du texte d'erreur, ignorer
  if [ -z "$tickets" ] || [[ "$tickets" != \[* ]]; then
    tickets="[]"
  fi

  local filter_desc
  if [ -n "$assignee" ]; then
    filter_desc="assigné à \"${assignee}\""
  else
    filter_desc="label \"${label}\""
  fi

  if [ "$tickets" = "[]" ] || [ "$tickets" = "null" ]; then
    cat <<EOF
Aucun ticket avec le ${filter_desc} n'est prêt à implémenter.

Pour déléguer un ticket à l'agent :
  bd label add <ID> ${label}

Pour voir tous les tickets ouverts :
  bd list -s open --json
EOF
  else
    cat <<EOF
Voici les tickets délégués à l'agent (${filter_desc}, statut ready) :

${tickets}

Workflow obligatoire :
1. bd show <ID>                  — lire le détail complet avant tout
2. bd update <ID> --claim        — clamer le ticket (atomique)
3. Implémenter + tester
4. bd update <ID> -s review      — passer en review
5. bd close <ID> --suggest-next  — clore après validation et passer au suivant

Commence par le ticket le plus prioritaire.
EOF
  fi
}

