#!/bin/bash
# Assemble le contenu d'un agent canonique (agents/) avec ses skills injectés.
# Architecture hybride :
#   - skills (Bucket A, obligatoires) → inlinées dans le body de l'agent
#   - native_skills (Bucket B, optionnelles) → déployées en .opencode/skills/<name>/SKILL.md
#   - stack skills (dynamiques, Bucket B) → déployées en .opencode/skills/ via stack-skills.json
# Usage : source ce fichier, puis appeler build_agent_content <agent_file>

# Guard contre le double sourcing
[[ -n "${_PROMPT_BUILDER_LOADED:-}" ]] && return 0
_PROMPT_BUILDER_LOADED=1

# Retourne la version du hub depuis config/hub.json
get_hub_version() {
  if command -v jq &>/dev/null && [ -f "$HUB_CONFIG" ]; then
    jq -r '.version // "unknown"' "$HUB_CONFIG" 2>/dev/null
  else
    grep '"version"' "$HUB_CONFIG" 2>/dev/null | head -1 \
      | sed 's/.*"version"[[:space:]]*:[[:space:]]*"\([^"]*\)".*/\1/' \
      || echo "unknown"
  fi
}

# Retourne le commentaire d'en-tête à insérer dans chaque fichier généré
build_generated_header() {
  local version; version=$(get_hub_version)
  echo "<!-- generated by opencode-hub v${version} — do not edit manually -->"
}

# Extrait la valeur d'une clé scalaire dans le frontmatter YAML
extract_frontmatter_value() {
  local file="$1" key="$2"
  sed -n '/^---$/,/^---$/p' "$file" | grep "^${key}:" | head -1 \
    | sed "s/^${key}:[[:space:]]*//"
}

# Extrait une liste inline [a, b, c] depuis le frontmatter
extract_frontmatter_list() {
  local file="$1" key="$2"
  extract_frontmatter_value "$file" "$key" \
    | tr -d '[]' | tr ',' '\n' \
    | sed 's/^[[:space:]]*//;s/[[:space:]]*$//' | grep -v '^$'
}

# Lit le frontmatter d'un agent en une seule passe et expose les variables :
#   _fm_id           : valeur du champ id
#   _fm_skills       : valeur brute du champ skills        (ex: "[skill/a, skill/b]")
#   _fm_native_skills: valeur brute du champ native_skills (ex: "[skill/c, skill/d]")
#   _fm_model        : valeur du champ model (optionnel)
#   _fm_raw          : le frontmatter complet (toutes les lignes, pour extract_permission_json)
# Utilise uniquement des builtins bash (read, while) — pas de subprocess.
# @param $1 — chemin absolu du fichier agent
read_agent_frontmatter() {
  local file="$1"
  _fm_id="" _fm_skills="" _fm_native_skills="" _fm_model="" _fm_raw=""
  local in_fm=0
  while IFS= read -r line; do
    if [ "$in_fm" -eq 0 ] && [ "$line" = "---" ]; then
      in_fm=1
      continue
    fi
    if [ "$in_fm" -eq 1 ] && [ "$line" = "---" ]; then
      break
    fi
    if [ "$in_fm" -eq 1 ]; then
      # Accumuler le frontmatter brut (pour extract_permission_json)
      _fm_raw="${_fm_raw}${line}"$'\n'
      
      # ⚠ Contrainte d'ordre : model: est optionnel et non inclus dans le early exit
      # (ligne ci-dessous). Il DOIT apparaître avant skills: dans le frontmatter
      # pour être lu avant la sortie anticipée.
      case "$line" in
        id:*)           _fm_id="${line#id:}";            _fm_id="${_fm_id# }"            ;;
        skills:*)       _fm_skills="${line#skills:}";    _fm_skills="${_fm_skills# }"    ;;
        native_skills:*) _fm_native_skills="${line#native_skills:}"; _fm_native_skills="${_fm_native_skills# }" ;;
        model:*)        _fm_model="${line#model:}";      _fm_model="${_fm_model# }"      ;;
      esac
    fi
    # Arrêter dès qu'on a tout (optimisation : les champs utiles sont dans les 10 premières lignes)
    # _fm_model est optionnel — pas inclus dans la condition de sortie anticipée
    # Note : on ne peut pas sortir avant la fin du frontmatter car on a besoin de _fm_raw complet
    # pour extract_permission_json. On optimise donc moins, mais on économise le sed.
  done < "$file"
}

# Retourne les items d'une liste frontmatter déjà lue en mémoire (valeur brute)
# Usage : _fm_list_items "[a, b, c]" → affiche a / b / c une par ligne
# Utilise tr + bash — pas de sed subprocess supplémentaire
_fm_list_items() {
  local raw="$1"
  # Supprimer [ et ], remplacer virgules par newlines, strip espaces
  printf '%s' "$raw" | tr -d '[]' | tr ',' '\n' | \
    while IFS= read -r item; do
      item="${item# }"; item="${item% }"
      [ -n "$item" ] && printf '%s\n' "$item"
    done
}

# Retourne un rang numérique pour la hiérarchie des modèles (fonction interne)
# Hiérarchie : claude-opus-4(3) > claude-sonnet-4-5(2) > claude-haiku-4-5(1)
# Modèles inconnus = rang 0 — volontairement inférieur à haiku pour que le clamp
# force systématiquement le plancher quand un modèle non reconnu est résolu.
# Cela évite qu'un modèle inconnu contourne silencieusement un plancher opus/sonnet.
_get_model_rank() {
  local model="$1"
  # Extraire le nom de base (sans préfixe provider comme "anthropic/")
  local base="${model##*/}"
  case "$base" in
    claude-opus-4*)     echo 3 ;;
    claude-sonnet-4-5*) echo 2 ;;
    claude-haiku-4-5*)  echo 1 ;;
    *)                  echo 0 ;;
  esac
}

# Applique le clamp (plancher) du frontmatter
# Si resolved_model < floor_model en rang → retourne floor_model + warning
# $1 = resolved_model (depuis la cascade), $2 = floor_model (depuis le frontmatter), $3 = agent_id (pour le log)
clamp_model() {
  local resolved="$1" floor="$2" agent_id="${3:-}"
  [ -z "$floor" ] && echo "$resolved" && return 0
  local rank_resolved rank_floor
  rank_resolved=$(_get_model_rank "$resolved")
  rank_floor=$(_get_model_rank "$floor")
  if [ "$rank_resolved" -lt "$rank_floor" ]; then
    # Collecter pour le récapitulatif (affiché en Phase 3 uniquement)
    _CLAMP_APPLIED_AGENTS="${_CLAMP_APPLIED_AGENTS:-}${agent_id}:${resolved}→${floor};"
    echo "$floor"
  else
    echo "$resolved"
  fi
}

# Retourne la famille d'un agent (nom du dossier parent dans agents/)
# Ex: agents/planning/orchestrator.md → "planning"
_get_agent_family() {
  local agent_file="$1"
  local dir="${agent_file%/*}"      # Équivalent dirname (bash pur, 0 subprocess)
  echo "${dir##*/}"                  # Équivalent basename
}

# Résout le modèle à utiliser pour un agent via une cascade à 7 niveaux + clamp.
# $1 = agent_file (chemin du fichier .md)
# $2 = project_id (optionnel)
# $3 = hub_agent_models (optionnel) — JSON object .agent_models.agents précalculé via tojson
#   ex: '{"orchestrator":"claude-opus-4"}' ou '{}' si la section est absente de hub.json.
# $4 = hub_family_models (optionnel) — JSON object .agent_models.families précalculé via tojson
#   ex: '{"planning":"claude-opus-4"}' ou '{}' si la section est absente.
# $5 = hub_global_model (optionnel) — string .opencode.model précalculé
# Si $3/$4/$5 sont fournis, les niveaux 4-6 lisent les valeurs en mémoire (zéro lecture hub.json).
# Si $3/$4/$5 sont vides, comportement actuel inchangé (lecture hub.json directe — rétrocompatibilité).
# Affiche le modèle résolu sur stdout.
resolve_agent_model() {
  local agent_file="$1"
  local project_id="${2:-}"
  local hub_agent_models="${3:-}"
  local hub_family_models="${4:-}"
  local hub_global_model="${5:-}"

  # Lire le frontmatter pour obtenir id et model (plancher)
  read_agent_frontmatter "$agent_file"
  local agent_id="${_fm_id:-$(basename "$agent_file" .md)}"
  local floor_model="${_fm_model:-}"
  local family
  family=$(_get_agent_family "$agent_file")

  local resolved=""
  local source=""

  # Niveaux 1-3 : projet (seulement si project_id non vide)
  if [ -n "$project_id" ]; then
    # 1. P.agents.X
    if [ -z "$resolved" ]; then
      resolved=$(_api_keys_get "$project_id" "agent_models.agents.${agent_id}")
      [ -n "$resolved" ] && source="project_agent"
    fi
    # 2. P.families.F
    if [ -z "$resolved" ]; then
      resolved=$(_api_keys_get "$project_id" "agent_models.families.${family}")
      [ -n "$resolved" ] && source="project_family"
    fi
    # 3. P.model
    if [ -z "$resolved" ]; then
      resolved=$(_api_keys_get "$project_id" "model")
      [ -n "$resolved" ] && source="project_global"
    fi
  fi

  # Niveaux 4-6 : hub.json
  # Si les valeurs précalculées sont fournies ($3/$4/$5), les utiliser directement (zéro lecture hub.json).
  # Sinon, conserver le comportement actuel (lecture hub.json directe — rétrocompatibilité).
  if [ -z "$resolved" ]; then
    if [ -n "$hub_agent_models" ] || [ -n "$hub_family_models" ] || [ -n "$hub_global_model" ]; then
      # Chemin rapide : valeurs précalculées en mémoire.
      # Note : hub_agent_models/hub_family_models peuvent valoir '{}' (objet JSON vide sérialisé)
      # quand la section correspondante est absente de hub.json. Dans ce cas le chemin rapide
      # est quand même activé, mais .[$id] // empty sur '{}' retourne vide → identique au chemin lent.
      # jq est nécessairement présent : le précalcul qui fournit ces paramètres est conditionné par command -v jq.
      if command -v jq &>/dev/null; then
        # 4. hub.agents.X — jq sur chaîne en mémoire (pas de lecture fichier)
        if [ -z "$resolved" ] && [ -n "$hub_agent_models" ]; then
          resolved=$(printf '%s' "$hub_agent_models" | jq -r --arg id "$agent_id" '.[$id] // empty' 2>/dev/null)
          [ -n "$resolved" ] && source="hub_agent"
        fi
        # 5. hub.families.F — jq sur chaîne en mémoire
        if [ -z "$resolved" ] && [ -n "$hub_family_models" ]; then
          resolved=$(printf '%s' "$hub_family_models" | jq -r --arg fam "$family" '.[$fam] // empty' 2>/dev/null)
          [ -n "$resolved" ] && source="hub_family"
        fi
      fi
      # 6. hub.model — valeur scalaire directe (zéro fork)
      if [ -z "$resolved" ]; then
        resolved="$hub_global_model"
        [ -n "$resolved" ] && source="hub_global"
      fi
    elif [ -f "$HUB_CONFIG" ]; then
      if command -v jq &>/dev/null; then
        # 4. hub.agents.X
        if [ -z "$resolved" ]; then
          resolved=$(jq -r ".agent_models.agents.\"${agent_id}\" // empty" "$HUB_CONFIG" 2>/dev/null)
          [ -n "$resolved" ] && source="hub_agent"
        fi
        # 5. hub.families.F
        if [ -z "$resolved" ]; then
          resolved=$(jq -r ".agent_models.families.\"${family}\" // empty" "$HUB_CONFIG" 2>/dev/null)
          [ -n "$resolved" ] && source="hub_family"
        fi
        # 6. hub.model
        if [ -z "$resolved" ]; then
          resolved=$(jq -r '.opencode.model // empty' "$HUB_CONFIG" 2>/dev/null)
          [ -n "$resolved" ] && source="hub_global"
        fi
      else
        # Fallback sans jq : sed pour extraire les sections puis grep la clé
        # 4. hub.agents.X — chercher agent_id dans la section "agents" de "agent_models"
        if [ -z "$resolved" ]; then
          resolved=$(sed -n '/"agent_models"/,/^[[:space:]]*}/p' "$HUB_CONFIG" 2>/dev/null \
            | sed -n '/"agents"/,/}/p' \
            | grep "\"${agent_id}\"" | head -1 \
            | sed 's/.*:[[:space:]]*"\([^"]*\)".*/\1/' | grep -v '{' || true)
          [ -n "$resolved" ] && source="hub_agent"
        fi
        # 5. hub.families.F — chercher family dans la section "families" de "agent_models"
        if [ -z "$resolved" ]; then
          resolved=$(sed -n '/"agent_models"/,/^[[:space:]]*}/p' "$HUB_CONFIG" 2>/dev/null \
            | sed -n '/"families"/,/}/p' \
            | grep "\"${family}\"" | head -1 \
            | sed 's/.*:[[:space:]]*"\([^"]*\)".*/\1/' | grep -v '{' || true)
          [ -n "$resolved" ] && source="hub_family"
        fi
        # 6. hub.model — chercher "model" dans la section opencode
        if [ -z "$resolved" ]; then
          resolved=$(sed -n '/"opencode"/,/}/p' "$HUB_CONFIG" 2>/dev/null | grep '"model"' | head -1 | sed 's/.*"model"[[:space:]]*:[[:space:]]*"\([^"]*\)".*/\1/' || true)
          [ -n "$resolved" ] && source="hub_global"
        fi
      fi
    fi
  fi

  # 7. Fallback hardcodé
  if [ -z "$resolved" ]; then
    resolved="claude-sonnet-4-5"
    source="fallback"
  fi

  # Clamp
  resolved=$(clamp_model "$resolved" "$floor_model" "$agent_id")
  
  # Retourner modèle|source
  echo "${resolved}|${source}"
}

# Extrait le bloc permission du frontmatter et le convertit en JSON inline
# Supporte : scalaires directs (question: allow) et sous-blocs map (bash: { "*": deny, "cmd": allow })
# Types supportés pour sous-blocs map : bash, read, edit, write, glob, task
# Retourne une chaîne JSON partielle, ex: "\"permission\": { \"question\": \"allow\" }"
# ou vide si aucune permission n'est déclarée
# Zéro fork dans la boucle : tous les tests utilisent case/[[ =~ ]] et les extractions
# de clé/valeur utilisent des expansions de paramètre bash (pas de echo|grep/sed/cut).
# @param $1 — chemin du fichier agent (utilisé si $2 est vide)
# @param $2 — frontmatter pré-lu (optionnel, pour éviter le sed)
extract_permission_json() {
  local file="$1"
  local frontmatter="${2:-}"
  
  # Si le frontmatter n'est pas fourni, l'extraire avec sed (rétrocompatibilité)
  if [ -z "$frontmatter" ]; then
    frontmatter=$(sed -n '/^---$/,/^---$/p' "$file" | sed '1d;$d')
  fi
  
  [ -z "$frontmatter" ] && return 0

  # Détecter si un bloc permission: existe — test builtin (zéro fork)
  # Note : cherche 'permission:' en début de ligne (newline Unix). Tab en lieu de newline : edge case non supporté.
  [[ "$frontmatter" == *$'\npermission:'* || "$frontmatter" == 'permission:'* ]] || return 0

  # Extraire tous les scalaires directs sous permission: (indentation 2 espaces)
  # et les sous-blocs map (indentation 4 espaces)
  # Scalaires supportés : question, bash, edit, write, read, glob, task (format clé: valeur)
  # Sous-blocs map supportés : bash, read, edit, write, glob, task (format clé: sans valeur, puis entrées indentées)
  local in_permission=0 current_subblock=""
  local scalar_fields=""
  local bash_entries="" read_entries="" edit_entries="" write_entries="" glob_entries="" task_entries=""

  while IFS= read -r line; do
    case "$line" in
      permission:*)
        in_permission=1; current_subblock=""; continue ;;
    esac

    if [ "$in_permission" = "1" ]; then
      # Quitter le bloc permission si on revient à l'indentation racine (caractère non-espace en tête)
      case "$line" in
        [!\ ]*)  in_permission=0; current_subblock=""; continue ;;  # retour indentation racine (ligne non indentée)
      esac

      # Détecter le début d'un sous-bloc map (2 espaces + nom + : sans valeur après)
      # Pattern : "  bash:" ou "  task:" (pas de valeur après le :)
      case "$line" in
        '  bash:')   current_subblock="bash"; continue ;;
        '  read:')   current_subblock="read"; continue ;;
        '  edit:')   current_subblock="edit"; continue ;;
        '  write:')  current_subblock="write"; continue ;;
        '  glob:')   current_subblock="glob"; continue ;;
        '  task:')   current_subblock="task"; continue ;;
      esac

      if [ -n "$current_subblock" ]; then
        # Ligne d'entrée de sous-bloc : "    \"*\": deny" ou "    \"bd list\": allow"
        # LIMITATION : les clés contenant ':' ne sont pas supportées (ex: "echo: test": allow)
        # car le parsing utilise le premier ':' comme séparateur clé/valeur.
        # En pratique, les clés sont des patterns de commande ou de fichier qui ne contiennent
        # jamais ':' dans les cas d'usage réels des permissions opencode.
        case "$line" in
          '    '*)
            # Extraire clé et valeur — expansions de paramètre bash (zéro fork)
            local kv="${line#"${line%%[! ]*}"}"   # strip leading whitespace
            
            # Ignorer les commentaires YAML (lignes commençant par #)
            # Ces commentaires causent des erreurs de parsing JSON s'ils contiennent ':'
            [[ "$kv" =~ ^# ]] && continue
            
            # Si la clé est entre guillemets, on doit trouver le ':' après le guillemet fermant
            # Format attendu : "clé avec : dedans": valeur
            local k v
            if [[ "$kv" =~ ^\"([^\"]*)\":[[:space:]]*(.*) ]]; then
              # Clé entre guillemets (peut contenir ':')
              k="${BASH_REMATCH[1]}"
              v="${BASH_REMATCH[2]}"
            else
              # Clé sans guillemets (ne peut pas contenir ':')
              k="${kv%%:*}"                    # tout avant le premier ':'
              k="${k#"${k%%[! ]*}"}"           # strip leading whitespace
              k="${k%"${k##*[! ]}"}"           # strip trailing whitespace
              v="${kv#*:}"                     # tout après le premier ':'
            fi
            
            # Nettoyer la valeur
            v="${v#"${v%%[! ]*}"}"             # strip leading whitespace
            v="${v%"${v##*[! ]}"}"             # strip trailing whitespace
            v="${v#\"}"                         # strip guillemet ouvrant si présent
            v="${v%\"}"                         # strip guillemet fermant si présent
            
            if [ -n "$k" ] && [ -n "$v" ]; then
              local entry="\"${k}\": \"${v}\""
              case "$current_subblock" in
                bash)  [ -n "$bash_entries" ] && bash_entries="${bash_entries}, "; bash_entries="${bash_entries}${entry}" ;;
                read)  [ -n "$read_entries" ] && read_entries="${read_entries}, "; read_entries="${read_entries}${entry}" ;;
                edit)  [ -n "$edit_entries" ] && edit_entries="${edit_entries}, "; edit_entries="${edit_entries}${entry}" ;;
                write) [ -n "$write_entries" ] && write_entries="${write_entries}, "; write_entries="${write_entries}${entry}" ;;
                glob)  [ -n "$glob_entries" ] && glob_entries="${glob_entries}, "; glob_entries="${glob_entries}${entry}" ;;
                task)  [ -n "$task_entries" ] && task_entries="${task_entries}, "; task_entries="${task_entries}${entry}" ;;
              esac
            fi
            ;;
          *)
            # Fin du sous-bloc (ligne non indentée à 4 espaces)
            current_subblock="" ;;
        esac
      fi

      # Scalaire direct sous permission: (2 espaces, pas dans un sous-bloc, format "clé: valeur")
      if [ -z "$current_subblock" ] && [[ "$line" =~ ^[[:space:]][[:space:]][a-zA-Z][a-zA-Z0-9_-]*:[[:space:]] ]]; then
        local sk="${line#  }"     # strip les 2 espaces d'indentation
        sk="${sk%%:*}"             # tout avant le premier ':'
        sk="${sk%"${sk##*[! ]}"}" # strip trailing whitespace
        local sv="${line#*:}"     # tout après le premier ':'
        sv="${sv#"${sv%%[! ]*}"}" # strip leading whitespace
        sv="${sv%%#*}"             # strip commentaire YAML (tout après #)
        sv="${sv%"${sv##*[! ]}"}" # strip trailing whitespace
        if [ -n "$sk" ] && [ -n "$sv" ]; then
          [ -n "$scalar_fields" ] && scalar_fields="${scalar_fields}, "
          scalar_fields="${scalar_fields}\"${sk}\": \"${sv}\""
        fi
      fi
    fi
  done <<< "$frontmatter"

  # Construire le JSON de permission
  local perm_fields="${scalar_fields}"
  
  # Ajouter chaque sous-bloc non vide comme objet JSON
  if [ -n "$bash_entries" ]; then
    [ -n "$perm_fields" ] && perm_fields="${perm_fields}, "
    perm_fields="${perm_fields}\"bash\": { ${bash_entries} }"
  fi
  if [ -n "$read_entries" ]; then
    [ -n "$perm_fields" ] && perm_fields="${perm_fields}, "
    perm_fields="${perm_fields}\"read\": { ${read_entries} }"
  fi
  if [ -n "$edit_entries" ]; then
    [ -n "$perm_fields" ] && perm_fields="${perm_fields}, "
    perm_fields="${perm_fields}\"edit\": { ${edit_entries} }"
  fi
  if [ -n "$write_entries" ]; then
    [ -n "$perm_fields" ] && perm_fields="${perm_fields}, "
    perm_fields="${perm_fields}\"write\": { ${write_entries} }"
  fi
  if [ -n "$glob_entries" ]; then
    [ -n "$perm_fields" ] && perm_fields="${perm_fields}, "
    perm_fields="${perm_fields}\"glob\": { ${glob_entries} }"
  fi
  if [ -n "$task_entries" ]; then
    [ -n "$perm_fields" ] && perm_fields="${perm_fields}, "
    perm_fields="${perm_fields}\"task\": { ${task_entries} }"
  fi

  if [ -n "$perm_fields" ]; then
    echo "\"permission\": { ${perm_fields} }"
  fi
}

# Retourne le contenu d'un fichier Markdown en ignorant le bloc frontmatter YAML
# Utilise le builtin read pour détecter "---" en première ligne (zéro fork).
# Le || true est intentionnel : IFS= read -r retourne exit 1 sur EOF (fichier vide),
# ce qui ferait planter la fonction sous set -euo pipefail sans ce guard.
strip_frontmatter() {
  local file="$1"
  local _first_line
  IFS= read -r _first_line < "$file" || true  # EOF retourne exit 1 — || true : safe avec set -e
  if [[ "$_first_line" = "---" ]]; then
    awk 'BEGIN{f=0;d=0} /^---$/{if(!f){f=1;next}else if(!d){d=1;next}} d{print}' "$file"
  else
    cat "$file"
  fi
}

# Retourne le mode de déploiement d'un agent (frontmatter mode:)
# Valeurs possibles : "primary" | "subagent" | "all"
# Fallback : "primary" si le champ est absent
get_agent_mode() {
  local mode
  mode=$(extract_frontmatter_value "$1" "mode")
  echo "${mode:-primary}"
}

# Retourne l'identifiant d'un agent (frontmatter id, ou nom de fichier sans extension)
get_agent_id() {
  local id
  id=$(extract_frontmatter_value "$1" "id")
  if [ -z "$id" ]; then
    basename "$1" .md
  else
    echo "$id"
  fi
}

# Détecte la stack d'un projet à partir de ses fichiers de dépendances.
# @param $1 — project_path (chemin racine du projet)
# Affiche sur stdout une liste de clés de stack, une par ligne.
# Clés possibles : typescript, python, vue, react, next, nuxt, angular,
#   nestjs, express, fastify, django, fastapi, laravel, rails, spring,
#   prisma, typeorm, sqlalchemy, mongoose,
#   vitest, jest, playwright, cypress, openapi,
#   react-native, flutter, swift, kotlin,
#   pandas, dbt, airflow, pyspark,
#   docker, github-actions, gitlab-ci,
#   terraform, kubernetes, helm, argocd
detect_stack() {
  local project_path="${1:-}"
  [ -z "$project_path" ] || [ ! -d "$project_path" ] && return 0

  local pkg_json="${project_path}/package.json"
  local pyproject="${project_path}/pyproject.toml"
  local requirements="${project_path}/requirements.txt"
  local gemfile="${project_path}/Gemfile"
  local build_gradle="${project_path}/build.gradle"
  local build_gradle_kts="${project_path}/build.gradle.kts"
  local pom_xml="${project_path}/pom.xml"
  local pubspec="${project_path}/pubspec.yaml"

  # Répertoires à exclure des find (node_modules, dist, build, .git, .nuxt...)
  # shellcheck disable=SC2054
  local _prune_args=( \( -name "node_modules" -o -name ".git" -o -name "dist" -o -name "build"
                         -o -name ".nuxt" -o -name ".output" -o -name ".next" -o -name "__pycache__"
                         -o -name ".venv" -o -name "venv" -o -name "vendor" \) -prune -o )

  # ── JavaScript / TypeScript (package.json) ──────────────────────────────
  if [ -f "$pkg_json" ]; then
    local deps=""
    deps=$(grep -E '"dependencies"|"devDependencies"' -A 9999 "$pkg_json" 2>/dev/null | \
